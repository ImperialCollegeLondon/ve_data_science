#' Title
#'
#' @param doi
#' @param decision
#' @param reason
#' @param tags
#' @param notes
#' @param outdir
#'
#' @returns
#'
#' @export
#' @examples

log_dataset <- function(
  doi,
  reason = NULL,
  notes = NULL,
  outdir = "data/derived/soil/validation/records"
) {
  # prompt to enter DOI
  doi <- readline("Enter DOI: ")

  # download dataset metadata
  meta <- rcrossref::cr_cn(doi, format = "bibentry")

  # prompt for decision, decision, decision...
  decision <- select.list(
    c("included", "excluded"),
    title = "Decision (enter 0 to skip): ",
    graphics = FALSE
  )
  if (decision == "") {
    decision <- "skipped"
  }

  # prompt for short-form reason
  reason <- select.list(
    c("used_elsewhere", "no_raw_data", "no_soil_data"),
    title = "Reason (enter 0 to skip): ",
    graphics = FALSE
  )

  # prompt for long-form notes
  notes <- readline("Notes (leave blank to skip): ")

  # Build record
  record <- list(
    doi = meta$doi,
    decision = decision,
    reason = reason,
    notes = notes,
    logged_at = format(Sys.time(), "%Y-%m-%d"),
    metadata = list(
      title = meta$title,
      author = meta$author,
      year = meta$year,
      journal = as.character(meta$journal %||% NA),
      publisher = meta$publisher,
      url = meta$url,
      keywords = meta$keywords
    )
  )

  # Write YAML
  slug <- gsub("[/.]", "-", meta$doi)
  path <- file.path(outdir, paste0(slug, ".yaml"))
  header <- paste0(
    "# Generated by log_dataset() in tools/R/log_dataset.R\n",
    "# Do not edit by hand — re-run the function to update\n"
  )
  yaml_text <- yaml::as.yaml(record)
  writeLines(paste0(header, yaml_text), path)

  # Completion message
  cli::cli_alert_info("Dataset from {doi} is {decision}")
  cli::cli_alert_success("Decision log saved to\n{path}")
}
